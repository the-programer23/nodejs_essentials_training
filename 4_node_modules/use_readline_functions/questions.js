const collectAnwers = require("./lib/collectAnwers");

const questions = [
    "What is you name?",
    "Where do you live?",
    "What are you going to do with Node js?"
];

const answerEvents = collectAnwers(questions);

answerEvents.on('answer', answer => {
    console.log(`Your answer is ${answer}`);
})

answerEvents.on('complete',  answers => {
    console.log('Thank you for your anwers. ');
    console.log(answers);
});

answerEvents.on('complete', () => {
    process.exit();
})